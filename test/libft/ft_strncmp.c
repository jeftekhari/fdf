#include "libft.h"

int		ft_strncmp(char const *s1, char const *s2, size_t n)
{
	while (n && *s1 && (*s1 == *s2))
	{
		s1++;
		s2++;
		n--;
	}
	if (n == 0)
		return (0);
	else
		return ((int)(unsigned char)*s1 - (int)(unsigned char)*s2);
}
